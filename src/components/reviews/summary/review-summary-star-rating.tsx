type Props = {
  rating: number
}

/**
 * @description 리뷰 요약 전용 별점 표시 (읽기 전용, 반응형)
 * 모바일: 작은 크기
 * 데스크탑: 큰 크기 (233x43px)
 */
export function ReviewSummaryStarRating({ rating }: Props) {
  const fullStars = Math.floor(rating)
  const hasHalfStar = rating % 1 >= 0.5
  const emptyStars = 5 - fullStars - (hasHalfStar ? 1 : 0)

  return (
    <div
      className="flex items-center gap-0.5 md:gap-[3.5px]"
      aria-label={`평점 ${rating}점 / 5점 만점`}
      role="img"
    >
      {/* 꽉 찬 별 */}
      {[...Array(fullStars)].map((_, i) => (
        <StarFullIcon key={`full-${i}`} />
      ))}

      {/* 반 별 */}
      {hasHalfStar && <StarHalfIcon key="half" />}

      {/* 빈 별 */}
      {[...Array(emptyStars)].map((_, i) => (
        <StarEmptyIcon key={`empty-${i}`} />
      ))}
    </div>
  )
}

